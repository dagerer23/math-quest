function buildLevel(
  id: string,
  grade: 1 | 2 | 3 | 4 | 5 | 6 | 7 | 8 | 9,
  chapter: string,
  sortOrder: number,
  isBoss: boolean,
  questions: Question[],
): Level {
  const knowledgePoints = [...new Set(questions.map(q => q.knowledgePoint))]
  const unitId = `unit-${grade}-${id.split('-')[1]}`
  return { id, chapter, grade, sortOrder, isBoss, questions, knowledgePoints, unitId }
}

